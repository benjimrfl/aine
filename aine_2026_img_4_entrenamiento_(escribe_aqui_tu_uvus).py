"""## Early stopping

El **early stopping** detiene el entrenamiento cuando la pérdida de validación (`val_loss`) no mejora durante un número de épocas consecutivas, llamado `patience`. Esto evita el sobreajuste y ahorra tiempo de cómputo.

La lógica es sencilla: en cada época se compara la `val_loss` con el mínimo registrado hasta ese momento. Si no mejora durante `patience` épocas seguidas, se detiene el entrenamiento y se restauran los pesos del mejor modelo.

"""

import copy

# Para provocar sobreajuste de forma visible usamos un modelo grande sin Dropout
# y un conjunto de entrenamiento muy pequeño (200 muestras)
train_es = DataLoader(Subset(train_dataset, range(200)), batch_size=32, shuffle=True)

modelo_es    = torch.nn.Sequential(
    torch.nn.Flatten(),
    torch.nn.Linear(28*28, 512),   # capa grande → más capacidad → más fácil sobreajustar
    torch.nn.ReLU(),
    torch.nn.Linear(512, 10)       # sin Dropout para que el sobreajuste sea claro
)
optimizer_es = torch.optim.SGD(modelo_es.parameters(), lr=5e-2)

patience      = 3          # épocas sin mejora antes de parar
max_epochs    = 50
mejor_vl      = float("inf")
mejores_pesos = None
sin_mejora    = 0

for epoch in range(max_epochs):
    tl_e = train_epoch(train_es,   modelo_es, fn_loss, optimizer_es)
    vl_e = eval_loss(val_loader, modelo_es, fn_loss)
    print(f"Época {epoch+1:2d}  train_loss={tl_e:.4f}  val_loss={vl_e:.4f}")

    if vl_e < mejor_vl:
        mejor_vl      = vl_e
        mejores_pesos = copy.deepcopy(modelo_es.state_dict())
        sin_mejora    = 0
    else:
        sin_mejora += 1
        if sin_mejora >= patience:
            print(f"\nEarly stopping en época {epoch+1}  "
                  f"(val_loss sin mejorar {patience} épocas consecutivas)")
            break

modelo_es.load_state_dict(mejores_pesos)   # restaura el mejor modelo
print(f"Mejor val_loss: {mejor_vl:.4f}")

