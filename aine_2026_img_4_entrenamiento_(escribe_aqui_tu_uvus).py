"""### Desactivación



Tenemos varias estrategias para controlar el proceso de propagación atrás mediante la desactivación (o activación) de las neuronas. Tenemos varias alternativas que nos permiten modificar el valor de los atributo requires_grad y grad_fn.

- torch.Tensor.requires_grad_(boolean) es un operador _in\_place_ que modifica el valor del atributo requires_grad  
- torch.Tensor.detach() devuelve un tensor con requires_grad=False
- torch.no_grad() evita la construcción del grafo (ignora el valor de los atributos grad y grad_fn)

"""

x = torch.rand(2, requires_grad=False)
linear = torch.nn.Linear(len(x), 2)

# --- requires_grad_(False): desactiva el cálculo del gradiente en un parámetro ---
linear.bias.requires_grad_(False)
y = linear(x)
y.mean().backward()
print("requires_grad_() → w.grad:", linear.weight.grad, "  b.grad:", linear.bias.grad)

# --- detach(): desconecta la salida del grafo ---
linear2 = torch.nn.Linear(len(x), 2)
y2 = linear2(x).detach()        # y2 no tiene grad_fn
print("detach()         → grad_fn:", y2.grad_fn,
      "  w.grad:", linear2.weight.grad, "  b.grad:", linear2.bias.grad)

# --- no_grad(): evita construir el grafo en todo el bloque ---
linear3 = torch.nn.Linear(len(x), 2)
with torch.no_grad():
    y3 = linear3(x)              # grad_fn=None aunque los parámetros tengan requires_grad=True
print("no_grad()        → grad_fn:", y3.grad_fn,
      "  w.grad:", linear3.weight.grad, "  b.grad:", linear3.bias.grad)

