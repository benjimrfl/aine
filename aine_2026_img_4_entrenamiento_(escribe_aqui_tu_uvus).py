"""### Backward

El método `backward()` de `torch.autograd` recorre el grafo en sentido inverso y calcula los gradientes de todos los nodos hoja con `requires_grad=True`.

Dado $y=w\cdot x + b$ siendo $x=6$, $w=2$ y $b=3$, los gradientes obtenidos son:
- $dy/dx= w = 2$
- $dy/dw= x = 6$
- $dy/db= 1$

Aspectos clave a tener en cuenta:
- Se aplica sobre un **tensor escalar** (la función de pérdida siempre lo es).
- **Acumula** los gradientes en `.grad` en llamadas sucesivas → hay que ponerlos a cero con `optimizer.zero_grad()` antes de cada época.

"""

