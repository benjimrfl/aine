"""## `WeightedRandomSampler`: corpus desbalanceados

Cuando las clases del corpus tienen frecuencias muy distintas (corpus desbalanceado), el modelo tiende a aprender mejor las clases mayoritarias e ignorar las minoritarias.

`WeightedRandomSampler` resuelve esto asignando a cada ejemplar un **peso inversamente proporcional a la frecuencia de su clase**, de modo que el DataLoader muestree con más frecuencia los ejemplares de clases minoritarias.

> **Nota**: se usa como alternativa a `shuffle=True` en el DataLoader (son mutuamente excluyentes).
"""

from collections import Counter

PATH_ROOT = "./datasets"

dataset = torchvision.datasets.MNIST(
    root=PATH_ROOT,
    train=True,
    download=True,
    transform=torchvision.transforms.ToTensor()
)

# Obtener las etiquetas de todo el corpus
etiquetas = dataset.targets.tolist()

# Contar la frecuencia de cada clase
frecuencias = Counter(etiquetas)
print("Frecuencia por clase:", dict(sorted(frecuencias.items())))

# Calcular el peso de cada clase (inversamente proporcional a su frecuencia)
num_clases = len(frecuencias)
peso_por_clase = {cls: 1.0 / count for cls, count in frecuencias.items()}

# Asignar a cada ejemplar el peso de su clase
pesos_ejemplares = torch.tensor([peso_por_clase[e] for e in etiquetas], dtype=torch.float)

# Crear el sampler
sampler = torch.utils.data.WeightedRandomSampler(
    weights=pesos_ejemplares,
    num_samples=len(pesos_ejemplares),
    replacement=True
)

# Usar el sampler en el DataLoader (sin shuffle=True)
dataloader_balanceado = torch.utils.data.DataLoader(
    dataset,
    batch_size=512,
    sampler=sampler   # No usar shuffle=True junto con sampler
)

# Comprobar que la distribución de la primera tanda está más equilibrada
_, labels_tanda = next(iter(dataloader_balanceado))
print("\nDistribución en primera tanda (balanceada):", Counter(labels_tanda.tolist()))

"""## `collate_fn`: agrupar ejemplares en una tanda

Por defecto, `DataLoader` agrupa los ejemplares de una tanda apilándolos con `torch.stack`, lo que **exige que todos los tensores tengan exactamente la misma forma**.

Cuando los ejemplares tienen tamaños distintos (imágenes sin redimensionar, secuencias de longitud variable, etc.) se produce un error. `collate_fn` es la función que podemos sustituir para controlar cómo se construye cada tanda.

Dos estrategias habituales:
- **Redimensionar/recortar** dentro del `collate_fn`.
- **Rellenar con ceros** (_padding_) hasta el tamaño máximo de la tanda.
"""

import torch
from PIL import Image
import torchvision

# Dataset de ejemplo con imágenes de distintos tamaños (sin transform fijo)
class DatasetTamañosVariables(torch.utils.data.Dataset):
    def __init__(self):
        # Simulamos 6 imágenes PIL de distintos tamaños
        self.imagenes = [
            Image.new("RGB", (w, h), (100, 150, 200))
            for w, h in [(40, 40), (60, 30), (50, 50), (80, 20), (35, 35), (70, 45)]
        ]
        self.etiquetas = [0, 1, 0, 1, 0, 1]

    def __len__(self):
        return len(self.imagenes)

    def __getitem__(self, idx):
        img_tensor = torchvision.transforms.ToTensor()(self.imagenes[idx])
        return img_tensor, self.etiquetas[idx]


# collate_fn que redimensiona todas las imágenes al tamaño fijo (H=50, W=50)
def collate_redimensiona(batch):
    resize = torchvision.transforms.Resize((50, 50), antialias=True)
    imagenes  = torch.stack([resize(img) for img, _ in batch])
    etiquetas = torch.tensor([label for _, label in batch])
    return imagenes, etiquetas


dataset_variable = DatasetTamañosVariables()

# Sin collate_fn → ERROR porque los tensores tienen formas distintas
# dataloader_error = torch.utils.data.DataLoader(dataset_variable, batch_size=3)
# next(iter(dataloader_error))  # RuntimeError: stack expects each tensor to be equal size

# Con collate_fn personalizado → funciona correctamente
dataloader_ok = torch.utils.data.DataLoader(
    dataset_variable,
    batch_size=3,
    collate_fn=collate_redimensiona
)

imgs, labels = next(iter(dataloader_ok))
