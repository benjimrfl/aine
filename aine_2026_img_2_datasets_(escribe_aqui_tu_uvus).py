import csv

class CustomDataset(torch.utils.data.Dataset):

    def __init__(self, annotations_file, data_dir, transform=None, target_transform=None):
        self.anotaciones = self._lector_csv(annotations_file)
        self.data_dir = data_dir
        self.transform = transform
        self.target_transform = target_transform

    def __len__(self):
        return len(self.anotaciones)

    def __getitem__(self, idx):

        image=self._recupera_imagen(idx)
        label=self._recupera_etiqueta(idx)

        if self.transform:
            image = self.transform(image)
        if self.target_transform:
            label = self.target_transform(label)

        return image, label


    def _recupera_imagen(self,idx):
        data_file_name=self.anotaciones[idx][0]
        data_path = os.path.join(self.data_dir, data_file_name)
        # image = torchvision.io.read_image(data_path,mode=mode)
        image=Image.open(data_path)
        image = image.convert('RGB')
        return image

    def _recupera_etiqueta(self,idx):
        return torch.tensor(int(self.anotaciones[idx][1]))

    def _lector_csv(self,file):
        with open(file, newline='') as f:
            reader = csv.reader(f,delimiter=",")
            # Ignora la primera línea del fichero csv (cabecera)
