"""## Definición de la red

El espacio de nombres torch.nn es donde se concentran todos los elementos necesarios para trabajar con redes neuronales

"""

# La red se organiza como un conjunto de módulos (contenedor de capas)
class NeuralNetwork(torch.nn.Module):
  def __init__(self):
    super().__init__()
    self.flatten=torch.nn.Flatten()
    self.linear_relu_stack=torch.nn.Sequential(
      torch.nn.Linear(28*28,512),
      torch.nn.ReLU(),
      torch.nn.Linear(512,512),
      torch.nn.ReLU(),
      torch.nn.Linear(512,10)
    )

