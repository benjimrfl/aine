"""### Formato de imagen: HWC vs CHW

Las imágenes en color se representan como arrays tridimensionales, pero existen dos convenciones para ordenar las dimensiones:

| Formato | Forma | Usado por |
|---------|-------|-----------|
| **HWC** (Height × Width × Channels) | `(alto, ancho, 3)` | PIL, matplotlib, numpy, OpenCV |
| **CHW** (Channels × Height × Width) | `(3, alto, ancho)` | **PyTorch** |

Es fundamental tener clara esta diferencia porque:
- Al cargar una imagen con PIL se obtiene formato HWC.
- PyTorch espera tensores en formato CHW para sus redes convolucionales.
- Al visualizar con matplotlib se necesita HWC de nuevo.

La transformación `torchvision.transforms.ToTensor()` realiza automáticamente la conversión HWC → CHW (y normaliza los valores de 0-255 a 0.0-1.0).
"""

from PIL import Image
import torch
import numpy as np

img_pil = Image.open("./img/torreDelOro.jpg")

# PIL → numpy (HWC)
img_np = np.array(img_pil)
print(f"Numpy (HWC): shape={img_np.shape}, dtype={img_np.dtype}, rango=[{img_np.min()}, {img_np.max()}]")

# PIL → Tensor PyTorch (CHW, float 0-1) mediante ToTensor()
to_tensor = torchvision.transforms.ToTensor()
img_tensor = to_tensor(img_pil)
print(f"Tensor (CHW): shape={img_tensor.shape}, dtype={img_tensor.dtype}, rango=[{img_tensor.min():.2f}, {img_tensor.max():.2f}]")

# Acceso a los canales individuales del tensor
canal_R = img_tensor[0]  # Primera "capa" = rojo
canal_G = img_tensor[1]  # Segunda "capa" = verde
canal_B = img_tensor[2]  # Tercera "capa" = azul
print(f"Canal R: shape={canal_R.shape}")

"""### Puente PIL ↔ Tensor

En la práctica necesitamos convertir entre PIL y tensores constantemente:
- **PIL → Tensor**: para alimentar la red neuronal (`transforms.ToTensor()`)
- **Tensor → PIL**: para visualizar resultados o guardar imágenes (`transforms.ToPILImage()`)

`permute(1, 2, 0)` convierte manualmente CHW → HWC (necesario para `matplotlib.imshow`).
"""

from PIL import Image
import torch
import torchvision
import matplotlib.pyplot as plt

img_pil = Image.open("./img/torreDelOro.jpg")

# ---- PIL → Tensor (CHW) ----
to_tensor = torchvision.transforms.ToTensor()
img_tensor = to_tensor(img_pil)
print(f"PIL → Tensor: {img_tensor.shape}  (CHW, float 0-1)")

# ---- Tensor → PIL ----
to_pil = torchvision.transforms.ToPILImage()
img_recuperada = to_pil(img_tensor)
print(f"Tensor → PIL: {img_recuperada.size}, modo={img_recuperada.mode}")

# ---- Visualización: tensor CHW → HWC con permute ----
img_hwc = img_tensor.permute(1, 2, 0)  # CHW → HWC
print(f"Tensor permutado (HWC): {img_hwc.shape}")

plt.figure(figsize=(12, 3))
plt.subplot(1, 3, 1)
plt.title("Original (PIL)")
plt.imshow(img_pil)
plt.axis("off")
plt.subplot(1, 3, 2)
plt.title("Tensor → permute → imshow")
plt.imshow(img_hwc)
plt.axis("off")
plt.subplot(1, 3, 3)
plt.title("Tensor → ToPILImage")
plt.imshow(img_recuperada)
plt.axis("off")
plt.tight_layout()
plt.show()

