class ClasificadorReviews():
    def __init__(self):
        nombre_modelo = "nlptown/bert-base-multilingual-uncased-sentiment"
        self.tokenizador = AutoTokenizer.from_pretrained(nombre_modelo)
        self.modelo = AutoModelForSequenceClassification.from_pretrained(nombre_modelo).to(device)

    def clasifica(self, texto):
        entrada = self.tokenizador(texto, return_tensors="pt", truncation=True, max_length=512).to(device)
        with torch.no_grad():
            salida = self.modelo(**entrada)

        prediccion = F.softmax(salida.logits, dim=-1)
        puntuacion = torch.argmax(prediccion, dim=-1).item() + 1

        return puntuacion


