def qa_manual(question, context):
    inputs = tokenizer_qa(question, context, return_tensors="pt")
    with torch.no_grad():
        outputs = model_qa(**inputs)

    answer_start_index = outputs.start_logits.argmax()
    answer_end_index = outputs.end_logits.argmax()

    predict_answer_tokens = inputs.input_ids[0, answer_start_index : answer_end_index + 1]
    answer = tokenizer_qa.decode(predict_answer_tokens)

    # Calculamos una puntuación de confianza básica
    score = torch.max(torch.softmax(outputs.start_logits, dim=-1)).item()
    return {'answer': answer, 'score': score}

