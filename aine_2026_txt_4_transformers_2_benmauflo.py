def download_file(url, basedir):
  with requests.get(url, stream=True) as r:
      if not os.path.isdir(basedir):
        os.makedirs(basedir)
      total_length = int(r.headers.get("Content-Length"))
      filename = url.split("/")[-1].split("?")[0]
