from typing import Any

def count_model_parameters(model: torch.nn.Module):
    """Count the memory usage of a PyTorch model in megabytes (MB).
       Note that this function does not include memory used for gradients during training or
       any temporary memory allocations that may occur during forward/backward passes.
    """

    # .numel() gives the number of elements in the tensor
    # .element_size() gives the size in bytes of each element
    params = sum(p.numel() * p.element_size() for p in model.parameters()) # Trainable parameters: weights, biases, etc.
    buffers = sum(b.numel() * b.element_size() for b in model.buffers()) # Non-trainable parameters: running stats, constants, etc.

    bytes_used = params + buffers

    number_of_parameters = sum(p.numel() for p in model.parameters())\
          + sum(b.numel() for b in model.buffers())
    print(f"The model has {number_of_parameters:,} parameters.")

    print(f"The model requires {bytes_used / 1024 ** 2:.2f} MB with dtype {model.dtype}")

def classify_text(tokenizer: Any, model: torch.nn.Module, text: str, device:str='cpu'):
  """
  Classify the sentiment of a given text using the provided tokenizer and model.
  Args:
      tokenizer (AutoTokenizer): The tokenizer to preprocess the text.
      model (torch.nn.Module): The pre-trained model for sequence classification.
      text (str): The input text to classify.
      device (str): The device to run the model on ('cpu' or 'cuda').
  """

  input = tokenizer(text, return_tensors="pt").to(device)
  print('\nText:', text)

  output = model(**input)

  score = max((v, i) for i,v in enumerate(output[0], start=1))[1]
  print('\nScore:', score)
  print('\nActivation outputs:', output[0].detach().cpu().numpy()[0])

