"""El objeto response devuelve un objeto con el contenido generado con un objeto de tipo `AIMessage` con el atributo `content` que contiene el texto generado.

### Paso 2: Definición del Estado del Sistema RAG

Para construir un flujo de procesamiento en LangGraph (librería para crear grafos de ejecución), necesitamos definir el estado que se pasará entre cada nodo del grafo.

En nuestro caso, el estado contiene:
- `question`: La pregunta del usuario que necesita respuesta.
- `context`: Los documentos recuperados de la base de datos vectorial que son relevantes para la pregunta.
- `answer`: La respuesta generada por el modelo de lenguaje basada en el contexto.

Esta estructura permite que cada función en el flujo pueda leer del estado, procesar la información, y escribir nuevos valores que serán consumidos por la siguiente función.
"""

from langchain_core.documents import Document
from typing import List, TypedDict

class State(TypedDict):
    question: str
    context: str
    answer: str

"""### Paso 3: Función de Recuperación

La función `retrieve()` es el primer paso del pipeline RAG. Su objetivo es:

1. Tomar la pregunta del usuario del estado.
2. Buscar en la base de datos vectorial los documentos más similares a la pregunta.
3. Filtrar solo aquellos documentos con una puntuación de relevancia suficientemente alta (≥ 0.75) para evitar incluir información irrelevante.
4. Actualizar el estado con los documentos recuperados.

Este paso es crucial porque el modelo generador solo usará estos documentos para responder, por lo que la calidad de la recuperación determina directamente la calidad de la respuesta final.
"""

def retrieve(state: State):
    retrieved_docs = vector_store.similarity_search_with_relevance_scores(state["question"], k=4)
    filtered_docs = [doc for doc, score in retrieved_docs if score >= 0.75]
    return {"context": filtered_docs}

"""### Paso 4: Función de Generación

La función `generate()` es el **segundo paso del pipeline RAG**. Su responsabilidad es:

1. Extraer el contexto recuperado del estado (los documentos relevantes).
2. Si no existe contexto, se proporciona una respuesta por defecto.
3. Si sí existe contexto, construir un prompt estructurado que incluya en la conversación:
   - Instrucciones generales.
   - El contexto (fragmentos de documentos recuperados).
   - La pregunta del usuario.
4. Pasar este prompt al modelo de lenguaje cuantizado.
5. Actualizar el estado con la respuesta proporcionada.


"""

from transformers import AutoTokenizer

tokenizer = AutoTokenizer.from_pretrained("unsloth/Llama-3.2-1B-Instruct")

def generate(state: State):
    docs_content = "\n\n".join(doc.page_content for doc in state["context"])

    if not docs_content: # If no context is retrieved
        return {"answer": "There is no information available in the text to answer this question.", "context": docs_content}

    # Prepare the conversation messages for the chat model
    messages = [
        {"role": "system", "content": "You are a helpful assistant that answers questions based solely on the provided context."}, # system prompt
        {"role": "assistant", "content": "Using the following context, answer the question clearly, paraphrasing the context and in English. If the information does not appear in the context, respond: 'There is no sufficient information in the text to answer this question.' Do not invent data or use external knowledge."},
        {"role": "assistant", "content": f"CONTEXT:{docs_content}"},
        {"role": "user", "content": state["question"]},
    ]
    prompt = tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
    response = llm.invoke(prompt,
                          temperature=0.0, # Deterministic output
                          )
    # The response contains an object with the generated content
    return {"answer": response.content, "context": docs_content}

"""### Paso 5: Grafo de Ejecución

Ahora integraremos los componentes anteriores en un **grafo de ejecución** usando LangGraph. Este grafo define:

1. **Estructura**: Las funciones `retrieve` y `generate` se ejecutan secuencialmente.
2. **Flujo de datos**: El estado se pasa automáticamente de una función a la siguiente, enriquecido con nueva información en cada paso.
3. **Compilación**: La función `compile()` optimiza el grafo para ejecución eficiente.

Cuando ejecutamos el grafo:
- Primero se ejecuta `retrieve()` que busca documentos relevantes.
- Luego se ejecuta `generate()` que produce la respuesta final.
- El usuario obtiene tanto la respuesta como el contexto utilizado para transparencia.
"""

from langgraph.graph import START, StateGraph

graph_builder = StateGraph(State).add_sequence([retrieve, generate])
graph_builder.add_edge(START, "retrieve")
graph = graph_builder.compile()

graph

