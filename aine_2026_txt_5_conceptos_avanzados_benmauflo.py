"""Los modelos de embeddings transforman palabras/frases en vectores numéricos multidimensionales de forma que palabras/frases similares semánticamente tengan vectores cercanos en el espacio. Esto se mide usando similitud de coseno, que calcula el ángulo entre dos vectores.

En el siguiente ejemplo, veremos cómo "Rey" es más similar a "King" (su traducción al inglés) que a "Puchero", demostrando que el modelo captura significado semántico.
"""

