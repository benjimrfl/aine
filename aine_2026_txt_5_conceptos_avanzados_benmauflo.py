"""### Instanciación de Herramientas

Definiremos una serie de herramientas para manipular y consultar una base de datos vectorial, calcular similitud entre palabras, y realizar operaciones de procesamiento de lenguaje natural.

Estas herramientas son:
- calcular_similitud: Calcula la similitud entre dos palabras utilizando embeddings.
- almacenar_embedding: Almacena el embedding de una palabra en la base de datos.
- eliminar_embedding: Elimina el embedding que contienen una palabra de la base de datos.
- consultar_documentos: Consulta los documentos que contienen una palabra en la base de datos y devuelve su contenido.
- modificar_elementos: Modifica los elementos que contienen una palabra en la base de datos, reemplazándola por otra.
- separa_palabras: Separa un texto en palabras individuales.
- tipo_palabras: Devuelve el tipo gramatical de cada palabra en un texto (opcionalmente filtrado por tipo: DET, ADP, NOUN,...).
- separa_frases: Separa un texto en frases individuales.
- contar_elementos: Cuenta el número de elementos en una lista.
"""

from langchain_huggingface import HuggingFaceEmbeddings
from langchain_chroma import Chroma
import numpy as np
import spacy

embedding_model = HuggingFaceEmbeddings(model_name="intfloat/multilingual-e5-large", model_kwargs={"device": "cpu"})

vector_store = Chroma(embedding_function=embedding_model, collection_name="aine_docs")

if spacy.util.is_package("es_core_news_lg") is False:
    spacy.cli.download("es_core_news_lg")

"""La forma más sencilla de definir herramientas en LangChain es utilizando el decorador @tool. A continuación, se muestran varias herramientas avanzadas que interactúan con el vector store y procesan texto utilizando spaCy.

> Nota: Es muy importante definir los tipos de las funciones para que LangChain los pueda interpretar correctamente, además de añadir descripciones claras de lo que hacen las funciones y sus parámetros. Esto es parte del "prompt engineering" que mejora la interacción con el modelo.
"""

from langchain.tools import tool
from langchain_core.documents import Document

def cosine_similarity(vec1, vec2):
    return np.dot(vec1, vec2) / (np.linalg.norm(vec1) * np.linalg.norm(vec2))

nlp = spacy.load('es_core_news_lg')
@tool("calcular_similitud")
def calcular_similitud(palabra1: str, palabra2: str) -> str:
    """
    Calcula la similitud entre dos palabras utilizando embeddings.
    """

    embedding_1 = np.array(embedding_model.embed_query(palabra1))
    embedding_2 = np.array(embedding_model.embed_query(palabra2))

    similarity = cosine_similarity(embedding_1, embedding_2)

    return f"La similitud entre '{palabra1}' y '{palabra2}' es {similarity:.4f}"

@tool("almacenar_embedding")
def almacenar_embedding(palabra: str) -> str:
    """
    Almacena el embedding de una palabra en la base de datos.
    """

    vector_store.add_documents([Document(page_content=palabra, metadata={"source": "user_input"})])

    return f"Embedding de la palabra '{palabra}' almacenado."

@tool("eliminar_embedding")
def eliminar_embedding(palabra: str) -> str:
    """
    Elimina el embedding que contienen una palabra de la base de datos.
    """

    vector_store.delete(where_document={"$contains": palabra})

    return f"Embedding de la palabra '{palabra}' eliminado."

@tool("consultar_documentos")
def consultar_documentos(palabra: str) -> list:
    """
    Consulta los documentos que contienen una palabra en la base de datos y devuelve su contenido.
    """
    results = vector_store.get(where_document={"$contains": palabra})
    return results["contents"]

@tool("modificar_elementos")
def modificar_elementos(palabra_origen: str, palabra_nueva: str) -> str:
    """
    Modifica los elementos que contienen una palabra en la base de datos, reemplazándola por otra.
    """
    results = vector_store.get(where_document={"$contains": palabra_origen})
    for doc_id, content in zip(results["ids"], results["documents"]):
        nuevo_contenido = content.replace(palabra_origen, palabra_nueva)
        vector_store.update_document(document_id=doc_id, document=Document(page_content=nuevo_contenido, metadata={"source": "user_update"}))
    return f"Elementos modificados: {results['ids']}"

@tool("separa_palabras")
def separa_palabras(texto: str) -> list:
    """
    Separa un texto en palabras individuales devolviendo una lista de elementos.
    """
    doc = nlp(texto)
    return [token.text for token in doc if token.is_alpha]

@tool("tipo_palabras")
def tipo_palabras(texto: str, tipo:str|None=None) -> list:
    """
    Devuelve el tipo gramatical de cada palabra listado de elementos (opcionalmente filtrado por tipo: DET, ADP, NOUN,...).
    """
    doc = nlp(texto)
    return [token.text + "-" + token.pos_ for token in doc if tipo is None or token.pos_ == tipo]

@tool("separa_frases")
def separa_frases(texto: str) -> list:
    """
    Separa un texto en frases individuales devolviendo una lista de elementos.
    """
    doc = nlp(texto)
    return [sent.text for sent in doc.sents]

@tool("contar_elementos")
def contar_elementos(elementos: list) -> int:
    """
    Cuenta el número de elementos en una lista.
    """
    return len(elementos)

tools = [calcular_similitud, almacenar_embedding, separa_palabras, tipo_palabras, separa_frases, eliminar_embedding, consultar_documentos, modificar_elementos, contar_elementos]

