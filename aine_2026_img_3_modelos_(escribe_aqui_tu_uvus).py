"""### Dropout

La dilución (_dropout_) consiste en _apagar_ (descartar) aleatoriamente un conjunto de neuronas de una red.
Habitualmente el mecanismo consiste en poner a cero el valor de salida de una neurona

La dilución es un mecanismo empleado para evitar el sobreajuste (_overfitting_) durante el entrenamiento.

La intución detrás de este proceso es la siguiente: al descartar o amortiguar la salida de ciertas neuronas de forma arbitraria obliga al resto de las neuronas a esforzarse para compensar el aprendizaje de las descartadas.

Pytorch ofrece varias mecanismos de dilución:
https://pytorch.org/docs/stable/nn.html#dropout-layers

La capa Dropout es una de las más empleadas. Su efecto consiste en:
- poner a cero las neuronas de una capa de forma aleatoria con una probabilidad $p$.
- compensar la pérdida de información de las neuronas apagadas de forma que el valor de las neuronas encendidas se ajustan proporcionalmente en un factor $\frac{1}{1-p}$


"""

## nn.Dropout
num_entradas=7

# Entrada simple
inputs = torch.rand(num_entradas)

# Entrada múltiple
# num_ejemplares=1
# inputs = torch.rand(num_ejemplares,num_entradas)

