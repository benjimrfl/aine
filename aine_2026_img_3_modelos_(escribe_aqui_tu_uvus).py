"""## Almacenamiento y recuperación

El método recomendado es serializar el **`state_dict`** (mapa de parámetros), no el modelo completo — es más portable y no depende de la definición exacta de la clase en disco.

| Operación | Código |
|---|---|
| Guardar | `torch.save(model.state_dict(), "ruta.pth")` |
| Recuperar | `model.load_state_dict(torch.load("ruta.pth"))` |

> **Importante**: el modelo destino debe tener **exactamente la misma arquitectura** que el guardado. Tras cargar, llamar a `model.eval()` antes de hacer inferencia.
"""

import os
ruta = "./modelos/model.pth"
os.makedirs("./modelos", exist_ok=True)

# --- Guardar ---
modelo = NeuralNetwork(num_entradas, num_salidas)
torch.save(modelo.state_dict(), ruta)
print("Guardado:", modelo.state_dict())

# --- Recuperar ---
# El modelo destino debe tener exactamente la misma arquitectura
modelo_cargado = NeuralNetwork(num_entradas, num_salidas)
modelo_cargado.load_state_dict(torch.load(ruta))
modelo_cargado.eval()

X = torch.rand(2, num_entradas)
with torch.no_grad():
    pred = modelo_cargado(X)
print(f"\nPredicción con modelo recuperado:\n{pred}")

# Verificar que los parámetros son idénticos tras el ciclo guardar/cargar
igual = all(torch.equal(p1, p2) for p1, p2 in zip(modelo.parameters(), modelo_cargado.parameters()))
print(f"¿Parámetros idénticos tras guardar/cargar? {igual}")

