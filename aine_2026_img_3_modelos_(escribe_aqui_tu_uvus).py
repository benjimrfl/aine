"""### Modos de ejecución: `train()` y `eval()`

Un modelo en PyTorch puede estar en dos modos:

| Modo | Activación | Efecto |
|---|---|---|
| `model.train()` | entrenamiento | Dropout activo, BatchNorm usa estadísticas del batch |
| `model.eval()` | inferencia | Dropout desactivado, BatchNorm usa estadísticas globales |

Por defecto, al crear un modelo está en modo `train()`. **Siempre** hay que llamar a `model.eval()` antes de usarlo para predicción, o los resultados serán incorrectos.

Además, durante la inferencia no es necesario calcular gradientes. Envolver el código con `torch.no_grad()` ahorra memoria y acelera la ejecución:

```python
model.eval()
with torch.no_grad():
    pred = model(X)
```
"""

# NeuralNetwork incluye Dropout
model = NeuralNetwork(num_entradas, num_salidas)
X = torch.rand(2, num_entradas)

# --- Modo train (por defecto) ---
model.train()
print(f"Modo training: {model.training}")   # True
out_train = model(X)
print("Salida en train (Dropout activo — varía en cada llamada):")
print(out_train)

# --- Modo eval ---
model.eval()
print(f"\nModo training: {model.training}")  # False

# torch.no_grad() desactiva el cálculo de gradientes (más rápido, menos memoria)
with torch.no_grad():
    out_eval = model(X)
print("Salida en eval (Dropout desactivado — estable):")
print(out_eval)

print(f"\n¿Son iguales train y eval? {torch.allclose(out_train, out_eval)}")

