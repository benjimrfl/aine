"""### BatchNorm2d

https://pytorch.org/docs/stable/generated/torch.nn.BatchNorm2d.html

La **normalización por lotes** (_batch normalization_) es una técnica que estabiliza y acelera el entrenamiento de redes profundas.

Se aplica **después de una convolución y antes de la función de activación**, normalizando la salida de cada canal:

$$\hat{x} = \frac{x - \mu_{\text{batch}}}{\sqrt{\sigma^2_{\text{batch}} + \varepsilon}}$$

y luego escala y desplaza con dos parámetros entrenables $\gamma$ y $\beta$:

$$y = \gamma\,\hat{x} + \beta$$

**Ventajas:**
- Reduce la sensibilidad a la tasa de aprendizaje.
- Actúa como regularizador (reduce la necesidad de Dropout en CNNs).
- Permite entrenar redes más profundas con más estabilidad.

**Parámetro principal:** `num_features` = número de canales de salida de la convolución anterior.

> ⚠️ Se comporta diferente según el modo:
> - `model.train()` → normaliza con la media/std del batch actual.
> - `model.eval()` → usa las medias/std acumuladas durante el entrenamiento (`running_mean`, `running_var`).
"""

import torch

num_canales_entrada = 1   # MNIST es monocromo
num_filtros = 8
kernel_size = 3
num_ejemplares = 2
alto = ancho = 28

inputs = torch.rand(num_ejemplares, num_canales_entrada, alto, ancho)

# Bloque típico: Conv2d → BatchNorm2d → ReLU
conv  = torch.nn.Conv2d(in_channels=num_canales_entrada, out_channels=num_filtros, kernel_size=kernel_size, padding=1)
bn    = torch.nn.BatchNorm2d(num_features=num_filtros)
relu  = torch.nn.ReLU()

out_conv = conv(inputs)
out_bn   = bn(out_conv)
out_relu = relu(out_bn)

print(f"Entrada:       {inputs.shape}")
print(f"Tras Conv2d:   {out_conv.shape}")
print(f"Tras BN:       {out_bn.shape}")
print(f"Tras ReLU:     {out_relu.shape}")

print(f"\nParámetros entrenables de BatchNorm2d (gamma y beta por canal):")
for name, param in bn.named_parameters():
    print(f"  {name}: {param.shape} — {param.numel()} parámetros")

print(f"\nEstadísticas acumuladas (no entrenables):")
print(f"  running_mean: {bn.running_mean}")
print(f"  running_var:  {bn.running_var}")

# Uso en un Sequential (patrón habitual en CNNs)
bloque = torch.nn.Sequential(
    torch.nn.Conv2d(1, 16, kernel_size=3, padding=1),
    torch.nn.BatchNorm2d(16),
    torch.nn.ReLU(),
    torch.nn.MaxPool2d(2),
)
print(f"\nBloque Conv+BN+ReLU+Pool: entrada {inputs.shape} → salida {bloque(inputs).shape}")

