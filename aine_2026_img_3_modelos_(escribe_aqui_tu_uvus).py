"""## Parámetros

Algunas capas transforman la entrada mediante valores ajustables llamados **parámetros**. Por ejemplo, en `nn.Linear` la transformación es $y = xW + b$, donde $W$ (pesos) y $b$ (sesgo) son parámetros.

Al crear el modelo se inicializan aleatoriamente. Durante el entrenamiento se actualizan iterativamente para minimizar el error.

Los parámetros son de tipo [`Parameter`](https://pytorch.org/docs/stable/generated/torch.nn.parameter.Parameter.html), una subclase de `Tensor` con `requires_grad=True` por defecto.

### Iteradores y conteo

`Module` suministra dos iteradores sobre sus parámetros:
- **`parameters()`** — recorre los tensores `Parameter`.
- **`named_parameters()`** — devuelve tuplas `(nombre, Parameter)`.

El número de parámetros es un indicador clave de la complejidad del modelo:
```python
total       = sum(p.numel() for p in model.parameters())
entrenables = sum(p.numel() for p in model.parameters() if p.requires_grad)
```
Los parámetros con `requires_grad=False` (congelados) no se actualizan durante el entrenamiento — útil en Transfer Learning.
"""

model = NeuralNetwork(num_entradas, num_salidas)
model.train()  # modo por defecto tras crear el modelo

# Recorrido con nombre y valor
print("Parámetros (nombre — forma — numel):")
for name, param in model.named_parameters():
    print(f"  {name:<30} {str(list(param.shape)):<15} → {param.numel():>4,}")

# Conteo total y entrenable
total       = sum(p.numel() for p in model.parameters())
entrenables = sum(p.numel() for p in model.parameters() if p.requires_grad)
print(f"\nTotal: {total:,}  |  Entrenables: {entrenables:,}")

# Acceso directo a un parámetro
print(f"\nBias de secuencia.0: {model.get_parameter('secuencia.0.bias')}")

"""### Mapa de estado

El mapa **state_dict()**  es un diccionario que relaciona cada capa con sus parámetros.

Este mapa permite consultar los parámetros de un modelo, pero será el medio para almacenar de forma permanente los parámetros de un modelo.

NOTA: El atributo requires_grad de los parámetros en el mapa de estado se almacena con valor False.


"""

