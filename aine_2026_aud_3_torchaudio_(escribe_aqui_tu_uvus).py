"""### Corpus: Uniformidad de la señal

https://pytorch.org/audio/stable/transforms.html

Torchaudio ofrece un conjunto de transformaciones de señales de audio por defecto.

Para uniformar todas las señales antes de usarlas, se aplican estas transformaciones en orden:
- `mix_down`: reduce la señal a formato mono (mezcla canales)
- `resample`: unifica la tasa de muestreo (`SAMPLE_RATE`)
- `cut_signal`: recorta la señal si supera `NUM_SAMPLES`
- `right_pad`: rellena con ceros por la derecha si faltan muestras hasta `NUM_SAMPLES`
"""

