from torch import nn

class NeuralNetwork(nn.Module):

    def __init__(self, num_classes=4):
        super().__init__()

        _canales_audio=1

        self.conv1 = nn.Sequential(
            nn.Conv2d(
                in_channels=_canales_audio,
                out_channels=16,
                kernel_size=3,
                stride=1,
                padding=2
            ),
            nn.ReLU(),
            nn.MaxPool2d(kernel_size=2)
        )

        self.conv2 = nn.Sequential(
            nn.Conv2d(
                in_channels=16,
                out_channels=32,
                kernel_size=3,
                stride=1,
                padding=2
            ),
            nn.ReLU(),
            nn.MaxPool2d(kernel_size=2)
        )
        self.flatten = nn.Flatten(1,-1)

        # El espectrograma de entrada tiene shape [1, 201, 111]:
        #   - 201 bins de frecuencia  (n_fft/2 + 1 = 400/2 + 1)
        #   - 111 frames temporales   (NUM_SAMPLES / hop_length + 1 = 22050/200 + 1)
        #
        # Tras conv1 (kernel=3, padding=2) + MaxPool2d(2):
        #   frecuencia: floor((201 + 4 - 2) / 2) = 101
        #   tiempo:     floor((111 + 4 - 2) / 2) = 56
        #
        # Tras conv2 (kernel=3, padding=2) + MaxPool2d(2):
        #   frecuencia: floor((101 + 4 - 2) / 2) = 51
        #   tiempo:     floor(( 56 + 4 - 2) / 2) = 29
        #
        # => 32 canales × 51 frecuencias × 29 tiempos = 47488 neuronas
        self.linear = nn.Linear(32*51*29, num_classes)

    def forward(self, input):

        # print(input.shape)
        x = self.conv1(input)
        # print(x.shape)
        x = self.conv2(x)
        # print(x.shape)
        x = self.flatten(x)
        logits= self.linear(x)

        # CrossEntropyLoss espera logits, no probabilidades softmax
        return logits

