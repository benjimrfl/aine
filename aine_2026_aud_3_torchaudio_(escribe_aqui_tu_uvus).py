def train_single_epoch(model, dataloader, loss_fn, optimiser):

    model.train()
    running_loss = 0.0
    for inputs, targets in dataloader:

        # CNN 2D espera [batch, canales, frecuencias, tiempo]
        if inputs.ndim != 4 or inputs.shape[1] != 1:
            raise ValueError(
                f"Shape de entrada no válido para CNN: {inputs.shape}. "
                "Ejecuta antes la sección 'Corpus: Uso de Espectrogramas'."
            )

        print('.', end='')
        logits = model(inputs)
        loss = loss_fn(logits, targets)

        optimiser.zero_grad()
        loss.backward()
        optimiser.step()

        running_loss += loss.item()

    return running_loss / len(dataloader)


def train(model, dataloader, loss_fn, optimiser, epochs):
    print("Inicio del entrenamiento")
    for i in range(epochs):
        print(f"Época {i+1} ", end='')
        print('.', end='')
        loss = train_single_epoch(model, dataloader, loss_fn, optimiser)
        print(f"\nLoss medio: {loss:.4f}")
