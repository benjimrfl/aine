"""## Vision Transformer (ViT)

Presentado en [_An Image is Worth 16×16 Words_](https://arxiv.org/abs/2010.11929) (Dosovitskiy et al., 2020), el Vision Transformer aplica el mecanismo de atención de los Transformers de NLP directamente a imágenes.

### Idea clave: parches como tokens

En lugar de procesar píxeles individualmente, la imagen se divide en **parches** fijos (p. ej. 16×16 píxeles) que se proyectan linealmente a un vector de _embedding_. Cada parche es el equivalente visual de una palabra en un Transformer de texto.

```
Imagen (224×224)  →  196 parches de 16×16  →  secuencia de 196 tokens
```

A estos tokens se añade:
- Un token especial **[CLS]** cuya representación final se usa para clasificar
- **Embeddings posicionales** aprendidos, para preservar la información espacial (los Transformers no son invariantes al orden)

### Bloque Transformer

Cada bloque aplica:
1. **Multi-Head Self-Attention (MSA)**: cada parche puede "prestar atención" a cualquier otro, capturando dependencias globales desde la primera capa (a diferencia de las CNN, que tienen receptivo campo local)
2. **Feed-Forward Network (FFN)**: dos capas lineales con GELU
3. **Layer Norm** antes de cada operación + **conexiones residuales**

### Comparación CNN vs ViT

| Aspecto | CNN | ViT |
|---------|-----|-----|
| Inductive bias | Localidad, equivarianza traslacional | Ninguno (más flexible, necesita más datos) |
| Receptivo campo | Crece capa a capa | Global desde la primera capa |
| Datos necesarios | Menos | Más (o preentrenamiento en ImageNet-21k) |
| Escalabilidad | Buena | Excelente (ViT-L, ViT-H) |

En `torchvision.models` están disponibles `vit_b_16`, `vit_b_32`, `vit_l_16`, `vit_l_32` y `vit_h_14`.
"""

