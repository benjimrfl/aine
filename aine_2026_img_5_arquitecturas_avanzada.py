### Inferencia y visualización de detecciones

import requests
from pathlib import Path
from io import BytesIO

def detectar_y_visualizar(ruta_imagen, modelo, categorias, umbral=0.5, device=device):
    """Ejecuta Faster R-CNN sobre una imagen y dibuja las detecciones."""
    img_pil = Image.open(ruta_imagen).convert("RGB")
    transform_det = pesos_rcnn.transforms()
    x = transform_det(img_pil).unsqueeze(0).to(device)

    with torch.no_grad():
        predicciones = modelo(x)[0]

    mascara      = predicciones["scores"] >= umbral
    cajas        = predicciones["boxes"][mascara].cpu()
    etiquetas    = predicciones["labels"][mascara].cpu()
    puntuaciones = predicciones["scores"][mascara].cpu()

    fig, ax = plt.subplots(1, 1, figsize=(10, 8))
    ax.imshow(img_pil)

    colores = plt.cm.tab20.colors
    for caja, lbl, score in zip(cajas, etiquetas, puntuaciones):
        x0, y0, x1, y1 = caja.tolist()
        color = colores[lbl.item() % len(colores)]
        rect = patches.Rectangle((x0, y0), x1 - x0, y1 - y0,
                                  linewidth=2, edgecolor=color, facecolor="none")
        ax.add_patch(rect)
        ax.text(x0, y0 - 4, f"{categorias[lbl.item()]} {score:.2f}",
                fontsize=8, color="white",
                bbox=dict(facecolor=color, alpha=0.7, pad=2, edgecolor="none"))

    ax.set_title(f"Faster R-CNN — {mascara.sum().item()} detecciones (umbral={umbral})", fontsize=11)
    ax.axis("off")
    plt.tight_layout()
    plt.show()
    print(f"Detecciones: {mascara.sum().item()}")
    for lbl, score in zip(etiquetas, puntuaciones):
        print(f"  {categorias[lbl.item()]:20s}  score={score:.3f}")


