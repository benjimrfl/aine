"""### Anchor boxes y propuestas del RPN

Antes de que el detection head clasifique y refine las cajas, el pipeline pasa por dos etapas de filtrado:

```
Anchor boxes (brutas)  →  RPN scoring + NMS  →  Proposals  →  RoI Align  →  Detecciones finales
```

**Anchor boxes** son cajas predefinidas que se colocan densa y sistemáticamente sobre cada posición de cada mapa de características del FPN. Para cada posición se generan cajas con distintos tamaños y proporciones (_aspect ratios_). En `fasterrcnn_resnet50_fpn` el `AnchorGenerator` usa:

- **5 escalas FPN** (P2–P6): tamaños de anchor `(32², 64², 128², 256², 512²)` píxeles
- **3 aspect ratios** por escala: `0.5, 1.0, 2.0`
- Resultado: **~200 000 anchors** para una imagen de 800×600

El **RPN** puntúa cada anchor (¿contiene objeto?) y aplica NMS con `rpn_nms_thresh=0.7`, reduciendo las ~200k anchors a ~1000–2000 **proposals** que se pasan al RoI Align.

"""

### Visualización: anchors brutos vs. proposals del RPN

from torchvision.models.detection.image_list import ImageList

img_pil_demo = Image.open(RUTA_DEMO).convert("RGB")
transform_det = pesos_rcnn.transforms()
x_det = transform_det(img_pil_demo).unsqueeze(0).to(device)

# ── 1. Anchors brutos del AnchorGenerator ─────────────────────────────────
with torch.no_grad():
    features = modelo_rcnn.backbone(x_det)                     # FPN features (dict P2–P6)

img_list = ImageList(x_det, [(x_det.shape[-2], x_det.shape[-1])])
all_anchors = modelo_rcnn.rpn.anchor_generator(img_list, list(features.values()))
anchors_img = all_anchors[0].cpu()   # (N_total, 4) — todas las anchors de la imagen

print(f"Total anchors generados : {len(anchors_img):,}")
print(f"  → escalas FPN × aspect ratios × posiciones espaciales")

# Escala imagen original → escala de entrada del modelo
w_orig, h_orig = img_pil_demo.size
h_model, w_model = x_det.shape[2], x_det.shape[3]
sx, sy = w_orig / w_model, h_orig / h_model

# ── 2. Proposals del RPN (tras scoring + NMS) ─────────────────────────────
proposals_capturadas = []

def _hook_rpn(module, input, output):
    # output = (proposals_list, losses_dict)
    proposals_capturadas.append(output[0][0].detach().cpu())

handle = modelo_rcnn.rpn.register_forward_hook(_hook_rpn)
with torch.no_grad():
    _ = modelo_rcnn(x_det)
handle.remove()

proposals = proposals_capturadas[0]   # (M, 4) en coordenadas del modelo
print(f"Proposals tras RPN NMS  : {len(proposals):,}")

# ── 3. Visualización ───────────────────────────────────────────────────────
fig, axes = plt.subplots(1, 2, figsize=(16, 7))

# — Izquierda: muestra aleatoria de anchors brutos (escalados a la imagen original)
N_MUESTRA = 150
idx_muestra = np.random.choice(len(anchors_img), N_MUESTRA, replace=False)
axes[0].imshow(img_pil_demo)
for idx in idx_muestra:
    x0, y0, x1, y1 = anchors_img[idx].tolist()
    x0, x1 = x0 * sx, x1 * sx
    y0, y1 = y0 * sy, y1 * sy
    rect = patches.Rectangle((x0, y0), x1 - x0, y1 - y0,
                              linewidth=0.5, edgecolor="yellow", facecolor="none", alpha=0.5)
    axes[0].add_patch(rect)
axes[0].set_title(f"Anchors brutos (muestra {N_MUESTRA} de {len(anchors_img):,})", fontsize=10)
axes[0].axis("off")

# — Derecha: proposals del RPN (escalados a la imagen original)
N_PROP = min(200, len(proposals))
axes[1].imshow(img_pil_demo)
for i in range(N_PROP):
    x0, y0, x1, y1 = proposals[i].tolist()
    x0, x1 = x0 * sx, x1 * sx
    y0, y1 = y0 * sy, y1 * sy
    rect = patches.Rectangle((x0, y0), x1 - x0, y1 - y0,
                              linewidth=0.7, edgecolor="cyan", facecolor="none", alpha=0.4)
    axes[1].add_patch(rect)
axes[1].set_title(f"Proposals RPN tras NMS (primeras {N_PROP} de {len(proposals):,})", fontsize=10)
axes[1].axis("off")

plt.suptitle("Antes vs. después del RPN: de ~200k anchors a ~1000 proposals", fontsize=11)
plt.tight_layout()
plt.show()

"""### YOLO con Ultralytics

**YOLO** (_You Only Look Once_) es la familia de detectores de **una sola etapa** más popular. A diferencia de Faster R-CNN (dos etapas: RPN + detection head), YOLO predice cajas y clases en un único paso por la red, lo que lo hace considerablemente más rápido.

La librería **Ultralytics** proporciona YOLOv8 / YOLO11 con una API unificada:

```python
from ultralytics import YOLO
modelo = YOLO("yolo11n.pt")   # n=nano, s=small, m=medium, l=large, x=xlarge
resultados = modelo("imagen.jpg")
```

El modelo se descarga automáticamente la primera vez (~6 MB para el nano). La salida incluye `boxes`, `masks`, `probs` y `keypoints` según la tarea configurada.

| Variante | Parámetros | Velocidad | mAP COCO |
|----------|-----------|-----------|----------|
| `yolo11n` | 2.6 M | ⚡⚡⚡ | 39.5 |
| `yolo11s` | 9.4 M | ⚡⚡ | 47.0 |
| `yolo11m` | 20.1 M | ⚡ | 51.5 |
| `yolo11l` | 25.3 M | — | 53.4 |

> **Nota**: Ultralytics no es parte de torchvision; se instala por separado (`pip install ultralytics`).

"""

