"""## Detección de objetos — Faster R-CNN

La **detección de objetos** no solo clasifica la imagen, sino que localiza cada objeto mediante una **caja delimitadora** (_bounding box_). La salida del modelo es un conjunto de tuplas (clase, confianza, caja).

### Arquitectura Faster R-CNN

Faster R-CNN (Ren et al., 2015) consta de tres bloques principales:

```
Imagen  →  Backbone  →  Feature Pyramid Network (FPN)  →  RPN  →  RoI Align  →  Detection Head
```

| Bloque | Función |
|--------|---------|
| **Backbone** (ResNet-50) | Extrae mapas de características a distintas escalas |
| **FPN** (Feature Pyramid Network) | Combina características de alta y baja resolución para detectar objetos de distintos tamaños |
| **RPN** (Region Proposal Network) | Propone ~2000 regiones candidatas que pueden contener un objeto (_anchors_) |
| **RoI Align** | Extrae un vector fijo por cada región propuesta |
| **Detection Head** | Clasifica cada región y refina las coordenadas de la caja |

### Salida

Para cada objeto detectado el modelo devuelve:
- `boxes`: tensor `[N, 4]` con coordenadas `[x_min, y_min, x_max, y_max]`
- `labels`: tensor `[N]` con el índice de clase en COCO (91 clases)
- `scores`: tensor `[N]` con la confianza de cada detección (0-1)

En `torchvision.models.detection` están disponibles `fasterrcnn_resnet50_fpn`, `fasterrcnn_mobilenet_v3_large_fpn` y `fasterrcnn_resnet50_fpn_v2`.
"""

